// //      In JavaScript, a token is the smallest unit of the language that the JavaScript engine can recognize, such as keywords, operators, identifiers, literals, or symbols
// // What is a Token in JavaScript
// Tokens include words, numbers, symbols (like +, -, =), and reserved keywords (like var, if, else).

// When JavaScript parses code, it breaks the source text into these tokens to understand and execute logic.

// Types of Tokens
// Identifiers: Names for variables and functions (e.g., myVar)

// Keywords: Reserved words for programming constructs (e.g., if, else, var)

// Literals: Constant values (e.g., numbers 5, strings 'Hello')

// Operators: Symbols for operations (e.g., +, =, &&)

// Separators: Symbols that separate statements or blocks (e.g., ;, ,, ())
 // comments
// Use Cases
// Tokens are fundamental for JavaScript parsing and error detection, such as “unexpected token” syntax errors when the parser finds an out-of-place token.


